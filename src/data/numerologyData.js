export function analyzePhoneNumber(phone) {
  const digits = phone.replace(/\D/g, '');
  if (digits.length < 9) return null;

  const sum = digits.split('').reduce((acc, d) => acc + parseInt(d), 0);
  let rootNum = sum;
  while (rootNum > 9 && rootNum !== 11 && rootNum !== 22) {
    rootNum = String(rootNum).split('').reduce((acc, d) => acc + parseInt(d), 0);
  }

  const data = numerologyData[rootNum] || numerologyData[9];

  // Check for special sequences
  const specialSeqs = Object.entries(luckySequences)
    .filter(([seq]) => digits.includes(seq))
    .map(([, meaning]) => meaning);

  // Digit breakdown
  const digitBreakdown = digits.split('').map(d => digitMeanings[parseInt(d)]);

  return {
    rootNumber:    rootNum,
    rawSum:        sum,
    data,
    specialSeqs,
    digitBreakdown,
    luckyScore:    Math.min(95, data.score + (specialSeqs.length * 3)),
  };
}
